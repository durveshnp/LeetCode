class Solution
{
    public:
    //Function to rotate a linked list.
    Node* rotate(Node* head, int k)
    {
        // Your code here
        
        
        
        
        k--;
        Node *cur = head;
        Node *prev = NULL;
        Node *nxt = NULL;
        
        while(k--)
        {
            cur = cur->next;
        }
        
        nxt = cur->next;
        
        if(nxt == NULL)
            return head;
        prev = nxt;
        cur->next = NULL;
        
        while(nxt->next)
        {
            nxt = nxt->next;
        }
        
        nxt->next = head;
        head = prev;
        
        return head;
    }
};
    


